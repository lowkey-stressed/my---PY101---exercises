# Write a program named age.py that includes someone's 
# age and then calculates and reports the future 
# age 10, 20, 30, and 40 years from now. 
# Here's the output for someone who is 22 years old.


age = int((input('How old are you? ')))

years1 = 10
years2 = 20
years3 = 30
years4 = 40


print(f'You are {age} years old.')
print(f'In {years1} you will be {age + 10} years old.')
print(f'In {years2} you will be {age + 20} years old.')
print(f'In {years3} you will be {age + 30} years old.')
print(f'In {years4} you will be {age + 40} years old.')
